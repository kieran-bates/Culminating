    private void goActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goActionPerformed
        
        if(selectionPhase == true)
        {
            go.setEnabled(false);
            buttonClick = 0;
            boolean repeat = true;
            while(repeat == true)
            {
                if(buttonClick != 0)
                {
                    playerShipOne = buttonClick;
                    buttonClick = 0;
                    while(buttonClick == 0)
                    {
                        if(buttonClick != 0)
                        {
                            playerShipTwo = buttonClick;
                            buttonClick = 0;
                            while(buttonClick == 0)
                            {
                                if(buttonClick != 0)
                                {
                                    playerShipThree = buttonClick;
                                    buttonClick = 0;
                                    while(buttonClick == 0)
                                    {
                                        if(buttonClick != 0)
                                        {
                                            playerShipFour = buttonClick;
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
            selectionPhase = false;
            output.setText("Your Move");
            go.setEnabled(true);
            
            
            
        }
        else
        {
            if (buttonClick == enemyShipOne||buttonClick == enemyShipTwo||buttonClick == enemyShipThree||buttonClick == enemyShipFour)
            {
                output.setText("Hit!");
                counter = counter+1;
                if(counter == 4)
                {
                    output.setText("You Win!");
                }
            }
            else
            {
                output.setText("Miss");
            }
        }
        
    }//GEN-LAST:event_goActionPerformed

